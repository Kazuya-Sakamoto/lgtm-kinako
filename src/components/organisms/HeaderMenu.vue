        >
        <nuxt-link
          to="/terms"
          class="text-gray-700 hover:bg-yellow-100 block px-4 py-2 text-sm"
          role="menuitem"
          tabindex="-1"
          id="menu-item-2"
          >利用規約</nuxt-link
        >
